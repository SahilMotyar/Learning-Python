friends = 10

friends += 1 #similar to friends = friends + 1
print(f"{friends}")
friends -= 2 #similar to friends = friends - 2
print(f"{friends}")
friends *= 3 #similar to friends = friends * 3
print(f"{friends}")
friends /= 2 #similar to friends = friends / 2
print(f"{friends}")
friends **= 2 # exponential 
print(f"{friends}")
remainder = friends % 3  # % finds the remainder of the divison

print(f"{remainder}")